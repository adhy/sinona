    exit('No direct script access allowed');

class Dinkes_kab extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        is_login();
        $this->load->model('Dinkes_kab_model');
        $this->load->library('form_validation');        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->template->load('template','dinkes_kab/t_dinkes_kab_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Dinkes_kab_model->json();
    }

    public function read($id) 
    {
        $row = $this->Dinkes_kab_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id_dinkes_kab' => $row->id_dinkes_kab,
		'nama_dinkes_kab' => $row->nama_dinkes_kab,
	    );
            $this->template->load('template','dinkes_kab/t_dinkes_kab_read', $data);
        } else {
            notif('2');
            redirect(site_url('dinkes_kab'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Simpan',
            'action' => site_url('dinkes_kab/create_action'),
	    'id_dinkes_kab' => set_value('id_dinkes_kab'),
	    'nama_dinkes_kab' => set_value('nama_dinkes_kab'),
	);
        $this->template->load('template','dinkes_kab/t_dinkes_kab_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'nama_dinkes_kab' => $this->input->post('nama_dinkes_kab',TRUE),
	    );

            $this->Dinkes_kab_model->insert($data);
            notif('0');
            redirect(site_url('dinkes_kab'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Dinkes_kab_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Perbaharui',
                'action' => site_url('dinkes_kab/update_action'),
		'id_dinkes_kab' => set_value('id_dinkes_kab', $row->id_dinkes_kab),
		'nama_dinkes_kab' => set_value('nama_dinkes_kab', $row->nama_dinkes_kab),
	    );
            $this->template->load('template','dinkes_kab/t_dinkes_kab_form', $data);
        } else {
            notif('2');
            redirect(site_url('dinkes_kab'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id_dinkes_kab', TRUE));
        } else {
            $data = array(
		'nama_dinkes_kab' => $this->input->post('nama_dinkes_kab',TRUE),
	    );

            $this->Dinkes_kab_model->update($this->input->post('id_dinkes_kab', TRUE), $data);
            notif('1');
            redirect(site_url('dinkes_kab'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Dinkes_kab_model->get_by_id($id);

        if ($row) {
            $this->Dinkes_kab_model->delete($id);
            notif('3');
            redirect(site_url('dinkes_kab'));
        } else {
            notif('2');
            redirect(site_url('dinkes_kab'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('nama_dinkes_kab', 'nama dinkes kab', 'trim|required');

	$this->form_validation->set_rules('id_dinkes_kab', 'id_dinkes_kab', 'trim');
	$this->form_validation->set_error_delimiters('<div class="has-error"><label class="text-danger"><i class="fa fa-times-circle-o"></i> ', '</label></div>');
    }

}

